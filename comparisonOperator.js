// Using the comparison operator
// In Javascript, the comparison operator is a double equal sign 
// whhich compares the values of the operands on the right and left sides.
// It returns true if both operands have the same value and false otherwise.
let stringValue = "true";
let result = (stringValue == "true"); //returns true 
console.log("result is: " + result);

let result2 = (stringValue == true);
console.log("result2 is: " + result2); //returns false





